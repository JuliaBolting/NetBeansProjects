public class Alunos {
    private String nome;
    private String moradia;
    
    public Alunos(){}

    public Alunos(String nome, String moradia) {
        this.nome = nome;
        this.moradia = moradia;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getMoradia() {
        return moradia;
    }

    public void setMoradia(String moradia) {
        this.moradia = moradia;
    }
    
    
}
