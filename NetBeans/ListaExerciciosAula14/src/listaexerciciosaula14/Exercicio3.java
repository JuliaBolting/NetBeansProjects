package listaexerciciosaula14;

/**
Faça um programa que mostre na tela os números ímpares de 1 a 100.
 */
public class Exercicio3 {

    public static void main(String[] args) {
        int num = 1;

        do {
            System.out.println(num);
            num = num+2;
        } while (num <= 100);

    }
    
}
