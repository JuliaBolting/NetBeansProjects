package listaexerciciosaula14;

/**
Faça um programa que mostre na tela todos os números inteiros de 55 a 118.
 */
public class Exercicio2 {

    public static void main(String[] args) {
        int num = 55;

        do {
            System.out.println(num);
            num++;
        } while (num <= 118);

    }
    
}
