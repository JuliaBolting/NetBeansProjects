package listaexerciciosaula14;

/**
 * Faça um programa que mostre na tela todos os números inteiros de 1 a 100 .
 */
public class Exercicio1 {

    public static void main(String[] args) {
        int num = 1;

        do {
            System.out.println(num);
            num++;
        } while (num <= 100);

    }

}
